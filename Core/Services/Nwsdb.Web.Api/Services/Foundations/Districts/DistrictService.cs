using Nwsdb.Web.Api.Brokers.DateTimes;
using Nwsdb.Web.Api.Brokers.Loggings;
using Nwsdb.Web.Api.Brokers.Storages;
using Nwsdb.Web.Api.Models.Districts;
using Nwsdb.Web.Api.Models.Lands;

namespace Nwsdb.Web.Api.Services.Foundations.Districts
{
    public partial class DistrictService : IDistrictService
    {
        private readonly IStorageBroker storageBroker;
        private readonly ILoggingBroker loggingBroker;
        private readonly IDateTimeBroker dateTimeBroker;

        public DistrictService(
            IStorageBroker storageBroker,
            ILoggingBroker loggingBroker,
            IDateTimeBroker dateTimeBroker)
        {
            this.storageBroker = storageBroker;
            this.loggingBroker = loggingBroker;
            this.dateTimeBroker = dateTimeBroker;
        }

        public IQueryable<District> RetrieveAllDistricts() =>
            TryCatch(() =>
                this.storageBroker.SelectAllDistrics());
    }
}
